// 4. generateImage
export const generateImage = async (prompt: string): Promise<string> => {
    const model = 'gemini-2.5-flash-image'; // For general, fast image generation
    
    const response: GenerateContentResponse = await apiWithRetry(() => getAiClient().models.generateContent({
        model,
        contents: {
            parts: [{ text: prompt }],
        },
        config: {
            responseModalities: [Modality.IMAGE],
        },
    }));
    
    for (const candidate of response.candidates ?? []) {
        for (const part of candidate.content.parts) {
            if (part.inlineData) {
                const base64ImageBytes: string = part.inlineData.data;
                const mimeType = part.inlineData.mimeType;
                return `data:${mimeType};base64,${base64ImageBytes}`;
            }
        }
    }

    throw new Error('Image generation failed: no image data received from the model.');
};


