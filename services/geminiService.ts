// Always use new GoogleGenAI({apiKey: process.env.API_KEY});
let aiInstance: GoogleGenAI | null = null;

const getAiClient = (): GoogleGenAI => {
  let key = '';
  if (typeof window !== 'undefined') {
    const localKey = localStorage.getItem('GOW_GEMINI_API_KEY');
    if (localKey && localKey.trim() !== '') {
      key = localKey;
    }
  }
  if (!key) {
    key = (process.env.GEMINI_API_KEY || process.env.API_KEY) as string;
  }
  if (!key || key === "PLACEHOLDER_KEY" || key.trim() === "") {
    throw new Error("MISSING_API_KEY");
  }
  if (!aiInstance) {
    aiInstance = new GoogleGenAI({
      apiKey: key,
      httpOptions: {
        headers: {
          'User-Agent': 'aistudio-build',
        }
      }
    });
  }
  return aiInstance;
};

// Helper to extract text from response and safely strip any markdown formatting
const getText = (response: GenerateContentResponse): string => {
  let text = response.text || '';
  // Strip any markdown HTML block fences if generated by the AI
  if (text.includes('```html')) {
    text = text.substring(text.indexOf('```html') + 7);
    if (text.includes('```')) {
      text = text.substring(0, text.indexOf('```'));
    }
  } else if (text.includes('```')) {
    text = text.substring(text.indexOf('```') + 3);
    if (text.includes('```')) {
      text = text.substring(0, text.indexOf('```'));
    }
  }
  return text.trim();
};

/**
 * A robust wrapper for API calls that implements a retry mechanism with exponential backoff.
 * This is crucial for handling API rate limits (quota errors) gracefully without failing immediately.
 * @param apiCall The function that makes the actual API call.
 * @returns The result of the API call.
 * @throws Throws an error if the API call fails after all retry attempts.
 */
const apiWithRetry = async <T>(apiCall: () => Promise<T>): Promise<T> => {
  let attempt = 0;
  const maxRetries = 3;
  const initialDelay = 1000; // 1 second

  while (attempt < maxRetries) {
    try {
      return await apiCall();
    } catch (error: any) {
      attempt++;
      // Check for common rate limit/quota error indicators in the error message
      const isQuotaError = error.toString().includes('429') || /quota|rate limit/i.test(error.toString());

      if (isQuotaError && attempt < maxRetries) {
        // Exponential backoff: 1s, 2s, 4s
        const delay = initialDelay * Math.pow(2, attempt - 1);
        console.warn(`Quota error detected. Retrying in ${delay}ms... (Attempt ${attempt}/${maxRetries})`);
        await new Promise(resolve => setTimeout(resolve, delay));
      } else {
        console.error("API call failed after multiple retries or for a non-quota reason.", error);
        // After final retry, or for non-quota errors, throw it to be handled by the UI
        throw error;
      }
    }
  }
  // This should theoretically not be reached, but it's a safeguard.
  throw new Error("API call failed after maximum retries.");
};


